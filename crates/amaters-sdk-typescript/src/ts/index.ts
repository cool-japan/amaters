/**
 * AmateRS TypeScript SDK
 *
 * This SDK provides TypeScript bindings for AmateRS, a Fully Homomorphic
 * Encrypted Database. It allows you to store and query encrypted data
 * without ever exposing the plaintext to the server.
 *
 * @example
 * ```typescript
 * import { AmateRSClient, Key, CipherBlob } from '@amaters/sdk';
 *
 * // Connect to server
 * const client = await AmateRSClient.connect('http://localhost:50051');
 *
 * // Set a value
 * await client.set('users', Key.fromString('user:123'), CipherBlob.fromBytes(data));
 *
 * // Get a value
 * const value = await client.get('users', Key.fromString('user:123'));
 * ```
 *
 * @packageDocumentation
 */

// Re-export WASM bindings (will be generated by wasm-pack)
// In a real build, these would come from the pkg/ directory

/**
 * Error codes for AmateRS operations
 */
export enum ErrorCode {
  /** Connection error */
  Connection = 1,
  /** Timeout error */
  Timeout = 2,
  /** Invalid argument error */
  InvalidArgument = 3,
  /** Not found error */
  NotFound = 4,
  /** Operation failed error */
  OperationFailed = 5,
  /** Serialization error */
  Serialization = 6,
  /** FHE operation error */
  FheError = 7,
  /** Configuration error */
  Configuration = 8,
  /** Internal error */
  Internal = 9,
  /** Unknown error */
  Unknown = 10,
}

/**
 * AmateRS error type
 */
export interface AmateRSError {
  /** Error code */
  readonly code: ErrorCode;
  /** Error message */
  readonly message: string;
  /** Optional cause */
  readonly cause?: string;
  /** Whether the error is retryable */
  readonly retryable: boolean;
  /** Error code as string */
  codeString(): string;
  /** Convert to JSON */
  toJSON(): string;
}

/**
 * Database key type
 *
 * Keys are immutable byte sequences used to identify data.
 */
export interface Key {
  /** Get the key as bytes */
  toBytes(): Uint8Array;
  /** Get the key as string (lossy conversion) */
  toString(): string;
  /** Key length in bytes */
  readonly length: number;
  /** Check if key is empty */
  isEmpty(): boolean;
  /** Check equality with another key */
  equals(other: Key): boolean;
  /** Compare with another key (-1, 0, 1) */
  compareTo(other: Key): number;
}

/**
 * Key factory functions
 */
export interface KeyConstructor {
  /** Create a key from string */
  fromString(s: string): Key;
  /** Create a key from bytes */
  fromBytes(data: Uint8Array): Key;
  /** Maximum key size (64KB) */
  readonly maxSize: number;
}

/**
 * Encrypted data blob
 *
 * Represents encrypted ciphertext data.
 */
export interface CipherBlob {
  /** Get the ciphertext as bytes */
  toBytes(): Uint8Array;
  /** Ciphertext length in bytes */
  readonly length: number;
  /** Check if blob is empty */
  isEmpty(): boolean;
  /** Verify integrity using checksum */
  verifyIntegrity(): boolean;
  /** Check equality with another blob */
  equals(other: CipherBlob): boolean;
}

/**
 * CipherBlob factory functions
 */
export interface CipherBlobConstructor {
  /** Create a cipher blob from bytes */
  fromBytes(data: Uint8Array): CipherBlob;
  /** Maximum blob size (1GB) */
  readonly maxSize: number;
}

