/**
 * Key-value pair for query results
 */
export interface KeyValuePair {
  /** The key */
  readonly key: Key;
  /** The value */
  readonly value: CipherBlob;
}

/**
 * Query result type
 */
export interface QueryResult {
  /** Check if this is a single value result */
  isSingle(): boolean;
  /** Check if this is a multi-value result */
  isMulti(): boolean;
  /** Check if this is a success result */
  isSuccess(): boolean;
  /** Get the single value (if applicable) */
  readonly value: CipherBlob | null;
  /** Get item count (for multi-value results) */
  readonly itemCount: number;
  /** Get affected rows (for success results) */
  readonly affectedRows: number;
  /** Get item at index */
  getItem(index: number): KeyValuePair | null;
  /** Get all items as array */
  getItems(): KeyValuePair[];
}

/**
 * Predicate type for filtering
 */
